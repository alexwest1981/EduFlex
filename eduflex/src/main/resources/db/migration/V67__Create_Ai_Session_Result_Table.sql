CREATE TABLE IF NOT EXISTS ai_session_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    course_id BIGINT,
    session_type VARCHAR(50) NOT NULL,
    score INT NOT NULL,
    max_score INT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ai_session_results_user FOREIGN KEY (user_id) REFERENCES app_users(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_ai_session_results_user_id ON ai_session_results(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_session_results_type ON ai_session_results(session_type);
CREATE INDEX IF NOT EXISTS idx_ai_session_results_course ON ai_session_results(course_id);
